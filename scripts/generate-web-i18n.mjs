#!/usr/bin/env node

import fs from "node:fs";
import path from "node:path";

const [catalogDirectory, outputFile] = process.argv.slice(2);
if (!catalogDirectory || !outputFile) {
  console.error("usage: node generate-web-i18n.mjs <locales-dir> <output-js>");
  process.exit(2);
}

const english = JSON.parse(
  fs.readFileSync(path.join(catalogDirectory, "en-US.json"), "utf8"),
);
const chinese = JSON.parse(
  fs.readFileSync(path.join(catalogDirectory, "zh-Hans.json"), "utf8"),
);
const translations = Object.fromEntries(
  Object.keys(english).map((key) => [english[key], chinese[key]]),
);
const templateTranslations = Object.keys(english)
  .filter((key) => /\{[A-Za-z][A-Za-z0-9_]*\}/.test(english[key]))
  .map((key) => ({ source: english[key], target: chinese[key] }));

const source = `// Generated by vectie/moonlib/scripts/generate-web-i18n.mjs.\n` +
`const catalogs = ${JSON.stringify({ "en-US": english, "zh-Hans": chinese }, null, 2)};\n` +
`const zhHans = ${JSON.stringify(translations, null, 2)};\n` +
`const zhHansTemplates = ${JSON.stringify(templateTranslations, null, 2)};\n` +
String.raw`
const COOKIE = "moonsuite_interface_locale";
const STORAGE = "moonsuite.interfaceLocale";
const WINDOW_NAME_PREFIX = "moonsuite-locale:";
const VALID_PREFERENCES = new Set(["system", "en-US", "zh-Hans"]);

function normalizedPreference(value) {
  return VALID_PREFERENCES.has(value) ? value : null;
}

function windowNamePreference() {
  return window.name.startsWith(WINDOW_NAME_PREFIX)
    ? normalizedPreference(window.name.slice(WINDOW_NAME_PREFIX.length))
    : null;
}

function selectedPreference() {
  const query = normalizedPreference(new URLSearchParams(location.search).get("locale"));
  const cookie = document.cookie.match(new RegExp("(?:^|; )" + COOKIE + "=([^;]+)"));
  const storedCookie = cookie ? normalizedPreference(decodeURIComponent(cookie[1])) : null;
  return query || storedCookie || windowNamePreference() ||
    normalizedPreference(localStorage.getItem(STORAGE)) || "system";
}

function selectedLocale() {
  const preference = selectedPreference();
  const raw = preference === "system"
    ? navigator.languages?.[0] || navigator.language || "en-US"
    : preference;
  return /^(zh(?:-|_)(?:Hans|CN|SG)|zh$)/i.test(raw) ? "zh-Hans" : "en-US";
}

function storeLocale(locale) {
  locale = normalizedPreference(locale) || "system";
  document.cookie = COOKIE + "=" + encodeURIComponent(locale) + "; Path=/; Max-Age=31536000; SameSite=Lax";
  localStorage.setItem(STORAGE, locale);
  window.name = WINDOW_NAME_PREFIX + locale;
  location.reload();
}

function escapeRegex(value) {
  return value.replace(/[.*+?^\x24{}()|[\]\\]/g, "\\$&");
}

function compileTemplate({ source, target }) {
  const names = [];
  let cursor = 0;
  let pattern = "^";
  for (const match of source.matchAll(/\{([A-Za-z][A-Za-z0-9_]*)\}/g)) {
    pattern += escapeRegex(source.slice(cursor, match.index)) + "(.+?)";
    names.push(match[1]);
    cursor = match.index + match[0].length;
  }
  pattern += escapeRegex(source.slice(cursor)) + "$";
  return { regex: new RegExp(pattern), names, target };
}

const zhTemplatePatterns = zhHansTemplates.map(compileTemplate);

function formatMessage(message, params = {}) {
  return message.replace(/\{([A-Za-z][A-Za-z0-9_]*)\}/g, (match, name) =>
    Object.prototype.hasOwnProperty.call(params, name) ? String(params[name]) : match,
  );
}

function message(key, params = {}) {
  const locale = selectedLocale();
  const template = catalogs[locale]?.[key] ?? catalogs["en-US"]?.[key] ?? key;
  return formatMessage(template, params);
}

function elementParams(element) {
  try {
    return JSON.parse(element.getAttribute("data-i18n-params") || "{}");
  } catch {
    return {};
  }
}

function translateKeyedElement(element) {
  const key = element.getAttribute("data-i18n");
  if (key) {
    const translated = message(key, elementParams(element));
    if (element.textContent !== translated) element.textContent = translated;
  }
  for (const attribute of ["placeholder", "title", "aria-label"]) {
    const attributeKey = element.getAttribute("data-i18n-" + attribute);
    if (attributeKey) {
      const translated = message(attributeKey, elementParams(element));
      if (element.getAttribute(attribute) !== translated) element.setAttribute(attribute, translated);
    }
  }
}

function translateText(text) {
  const leading = text.match(/^\s*/)?.[0] || "";
  const trailing = text.match(/\s*$/)?.[0] || "";
  const core = text.slice(leading.length, text.length - trailing.length);
  if (core in zhHans) return leading + zhHans[core] + trailing;
  for (const template of zhTemplatePatterns) {
    const match = core.match(template.regex);
    if (!match) continue;
    const params = Object.fromEntries(template.names.map((name, index) => [name, match[index + 1]]));
    return leading + formatMessage(template.target, params) + trailing;
  }
  return text;
}

function translate(root) {
  if (root.closest?.('[data-i18n-scope="content"],[data-i18n-scope="technical"],[translate="no"]')) return;
  for (const element of root.querySelectorAll?.("[data-i18n],[data-i18n-placeholder],[data-i18n-title],[data-i18n-aria-label]") || []) {
    if (!element.closest('[data-i18n-scope="content"],[data-i18n-scope="technical"],[translate="no"]')) {
      translateKeyedElement(element);
    }
  }
  if (root.matches?.("[data-i18n],[data-i18n-placeholder],[data-i18n-title],[data-i18n-aria-label]")) {
    translateKeyedElement(root);
  }
  if (selectedLocale() !== "zh-Hans") return;
  const walker = document.createTreeWalker(root, NodeFilter.SHOW_TEXT, {
    acceptNode(node) {
      return /\S/.test(node.nodeValue || "") &&
        !["SCRIPT", "STYLE", "CODE", "PRE", "TEXTAREA"].includes(node.parentElement?.tagName) &&
        !node.parentElement?.closest('[data-i18n-scope="content"],[data-i18n-scope="technical"],[translate="no"]') &&
        !node.parentElement?.closest("[data-i18n]")
        ? NodeFilter.FILTER_ACCEPT
        : NodeFilter.FILTER_REJECT;
    },
  });
  const nodes = [];
  while (walker.nextNode()) nodes.push(walker.currentNode);
  for (const node of nodes) {
    const translated = translateText(node.nodeValue || "");
    if (node.nodeValue !== translated) node.nodeValue = translated;
  }
  for (const element of root.querySelectorAll?.("[placeholder],[title],[aria-label]") || []) {
    for (const attribute of ["placeholder", "title", "aria-label"]) {
      if (element.hasAttribute(attribute)) {
        const translated = translateText(element.getAttribute(attribute) || "");
        if (element.getAttribute(attribute) !== translated) element.setAttribute(attribute, translated);
      }
    }
  }
  if (root.matches?.("[placeholder],[title],[aria-label]")) {
    for (const attribute of ["placeholder", "title", "aria-label"]) {
      if (root.hasAttribute(attribute)) {
        const translated = translateText(root.getAttribute(attribute) || "");
        if (root.getAttribute(attribute) !== translated) root.setAttribute(attribute, translated);
      }
    }
  }
}

function installSelector() {
  if (document.getElementById("moonsuite-language")) return;
  const label = document.createElement("label");
  label.id = "moonsuite-language";
  const languageLabel = selectedLocale() === "zh-Hans" ? "界面语言" : "Interface language";
  label.setAttribute("aria-label", languageLabel);
  Object.assign(label.style, {
    position: "fixed", right: "14px", bottom: "14px", zIndex: "2147483647",
    padding: "6px", border: "1px solid rgba(148,163,184,.45)", borderRadius: "10px",
    background: "rgba(255,255,255,.94)", boxShadow: "0 8px 28px rgba(15,23,42,.16)",
  });
  const select = document.createElement("select");
  select.setAttribute("aria-label", languageLabel);
  select.style.cssText = "border:0;background:transparent;color:#0f172a;font:600 13px system-ui;cursor:pointer";
  select.innerHTML = '<option value="system">System / 跟随系统</option><option value="en-US">English</option><option value="zh-Hans">简体中文</option>';
  select.value = selectedPreference();
  select.addEventListener("change", () => storeLocale(select.value));
  label.append(select);
  document.body.append(label);
}

function boot() {
  const locale = selectedLocale();
  window.name = WINDOW_NAME_PREFIX + selectedPreference();
  document.documentElement.lang = locale;
  document.documentElement.dir = "ltr";
  if (locale === "zh-Hans") {
    document.body.style.fontFamily = "'PingFang SC','Microsoft YaHei','Noto Sans CJK SC',system-ui,sans-serif";
  }
  translate(document.body);
  installSelector();
  new MutationObserver((records) => {
    for (const record of records) {
      if (record.type === "childList") for (const node of record.addedNodes) {
        if (node.nodeType === Node.ELEMENT_NODE) translate(node);
        else if (node.nodeType === Node.TEXT_NODE && node.parentElement) translate(node.parentElement);
      }
      if (record.type === "characterData" && record.target.parentElement) {
        translate(record.target.parentElement);
      }
      if (record.type === "attributes" && record.target.nodeType === Node.ELEMENT_NODE) {
        translate(record.target);
      }
    }
  }).observe(document.body, {
    childList: true,
    characterData: true,
    attributes: true,
    attributeFilter: ["placeholder", "title", "aria-label", "data-i18n", "data-i18n-params"],
    subtree: true,
  });
}

window.MoonSuiteI18n = Object.freeze({
  locale: selectedLocale,
  preference: selectedPreference,
  message,
  translate,
});

if (document.readyState === "loading") document.addEventListener("DOMContentLoaded", boot);
else boot();
`;

fs.writeFileSync(outputFile, source);
console.log(`generated ${outputFile} (${Object.keys(translations).length} messages)`);
